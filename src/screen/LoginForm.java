    private void jBAccesarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBAccesarActionPerformed
        // TODO add your handling code here:
        Usuario usuario=new UsuarioDaoImp().obtenerUsuario(jTFUsuario.getText());
        if(usuario!=null){
            if(usuario.getActivo()!=0){
                //Verificar contrase単a
                if(new UsuarioDaoImp().verificarContrase単a(usuario,jPFContrasenia.getText())){
                ContenedorPrincipal cont = new ContenedorPrincipal(usuario);
                Dimension dim = Toolkit.getDefaultToolkit().getScreenSize();
                cont.setLocation(dim.width/2-cont.getSize().width/2, dim.height/2-cont.getSize().height/2);
                cont.setSize(dim);
                cont.setVisible(true);
                this.dispose();
                }
            }
            else{
                mostrarLoginFailed();
            }
        }
        else{
            //Mandar mensaje
            mostrarLoginFailed();
        }
    }//GEN-LAST:event_jBAccesarActionPerformed

