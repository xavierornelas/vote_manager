    private void jBGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBGuardarActionPerformed
        // TODO add your handling code here:
        Perfil perfil = new Perfil();
        perfil.setNombre(jTFNombre.getText());
        perfil.setTelefono(jTFTelefono.getText());
        perfil.setClaveElectoral(jTFClaveElectoral.getText());
        perfil.setIdRol(obtenerIdRol(jCBRol.getSelectedItem().toString()));
        perfil.setIdStatusPerfil(obtenerIdStatus(jCBStatus.getSelectedItem().toString()));
        perfil.setIdUsuario(this.usuario.getIdUsuario());
        perfil.setActivo(1);
        if(!verificarNoNulos(perfil)){
            if(new PerfilDaoImp().agregarPersonalidad(perfil, this.idSeccional, this.usuario.getIdUsuario())){
                //Mensaje de insertado correctamente
                JOptionPane.showMessageDialog(null, "Se registró correctamente la personalidad", "Registro éxitoso", JOptionPane.OK_OPTION);
                this.dispose();
            }
            else{
                //No insertado correctamente
                JOptionPane.showMessageDialog(null, "No se insertó registro, contacte a su soporte técnico", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
        
    }//GEN-LAST:event_jBGuardarActionPerformed

