    private Boolean verificarNoNulos(Perfil perfil){
        Boolean nulos=false;
        if(perfil.getNombre().equals(""))
            nulos=true;
        if(perfil.getClaveElectoral().equals(""))
            nulos=true;
        if(perfil.getTelefono().equals(""))
            nulos=true;
        if(perfil.getIdRol()==0 && perfil.getIdStatusPerfil()==0)
            nulos=true;
        
        return nulos;
    }
    private Integer obtenerIdRol(String texto){
        return Integer.parseInt(texto.split(":")[0]);
    }
    private Integer obtenerIdStatus(String texto){
        return Integer.parseInt(texto.split(":")[0]);
    }
